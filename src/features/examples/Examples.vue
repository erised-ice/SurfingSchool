<script>
import Layout from '@components/common/Layout/Layout' // импорты всегда после открывадщего тэго скрипт
import Button from './components/Button';

export default {
  name: 'Examples', // name всегда строка - поэтому в кавычках
  components: {
    'app-layout': Layout, // здесь можно в кавычках придумать любое название компонента, которое потом можно использовать в ХТМЛ, как тэг.
    'example-button': Button
  },
  data() { // локальное состояние компонета.  https://ru.vuejs.org/v2/guide/components.html#%D0%A1%D0%B2%D0%BE%D0%B9%D1%81%D1%82%D0%B2%D0%BE-data-%D0%B4%D0%BE%D0%BB%D0%B6%D0%BD%D0%BE-%D0%B1%D1%8B%D1%82%D1%8C-%D1%84%D1%83%D0%BD%D0%BA%D1%86%D0%B8%D0%B5%D0%B9
    return {
      isBlueButton: true,
      isShowText: false,
      buttonText: 'изменятся состояние компонента'
    }
  },
  computed: { // computed это вычисляемые свойства VUE, в отличии от Data они могут изменяться из любого другого компонента, зависеть от пропсов и прочее. Data - это только локальное состояние компонента, которое НЕ изменяется извне!!!! Computed может изменяться снаружи
    isFriday() {
      return new Date().getDay() >= 4 // а в пятницу этот текст уже будет сразу показываться))
    }
  },
  methods: { // это МЕТОДЫ, они, как функции в JS. Тут выполняется что-то, что изменяет локальное состояние компонента, которое хранится в data     https://ru.vuejs.org/v2/api/#methods
    changeButtonColor() {
      this.isBlueButton = !this.isBlueButton // внутри экземпляра VUE, а именно внутри export мы всегда ссылаемся на this. this - это значит наш vue объект. Можешь вывести в консоль и посмотреть. console.log(this)
    },

    showText() {
      this.isShowText = !this.isShowText;
    }
  }
}
</script>

