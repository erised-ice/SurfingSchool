    <!-- Третий вариант передачи isSmall. Когда тебе нужно передать БУЛЕАН, в частности true, ты можешь вообще не писасть true, ты можешь просто указать название пропса -->



    <!-- Пропсы можно динамически изменять! И тогда они у тебя будет меняться внутри компонента. Смотрим пример -->
    <hr>
    <div style="padding: 40px 0">
      <button
        style="padding: 20px"
        v-on:click="changeButtonColor"
      >
        при нажатии цвет кнопки снизу поменяется
      </button>
      <!-- События во VUE аналогичны событиям в JS, которые раньше ставили в HTML при помощи обработчика onclick, здесь используется специальная vue-директива v-on https://ru.vuejs.org/v2/api/#v-on

        changeButtonColor - это метод, который есть в объекте methods внутри тэга script

        Также можно использовать синтаксис сокращения и написать @click вместо v-on:click   https://ru.vuejs.org/v2/guide/syntax.html#%D0%A1%D0%BE%D0%BA%D1%80%D0%B0%D1%89%D0%B5%D0%BD%D0%B8%D0%B5-v-on   - там можно прочитать и другие сокращения, например просто двоеточие это на самом деле v-bind
        -->
      <br />
